package cn.itcast.day08.demo05;

public class Demo03Math {
    public static void main(String[] args) {
        System.out.println(Math.abs(-1.2)); // 绝对值
        System.out.println(Math.ceil(1.1)); // 2 向上取整
        System.out.println(Math.floor(1.9)); // 1  向下取整
        System.out.println(Math.round(20.4)); // 近似值
    }

}
