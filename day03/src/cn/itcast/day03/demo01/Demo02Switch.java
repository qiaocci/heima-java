package cn.itcast.day03.demo01;

public class Demo02Switch {
    public static void main(String[] args) {
        int weekday = 3;

        switch (weekday) {
            case 1:
                System.out.println("周一");
            case 2:
                System.out.println("周二");
            case 3:
                System.out.println("周三");

        }
    }

}
