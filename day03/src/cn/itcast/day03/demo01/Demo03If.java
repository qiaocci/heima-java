package cn.itcast.day03.demo01;

public class Demo03If {
    public static void main(String[] args) {
        for (int i = 1; i < 10; i++) {
            System.out.println(i);
        }
        int i2 = 10;
        while (i2 > 0) {
            i2 -= 1;
            System.out.println(i2);
        }
    }

}
